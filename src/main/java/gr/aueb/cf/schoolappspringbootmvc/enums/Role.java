package gr.aueb.cf.schoolappspringbootmvc.enums;

/**
 * Enumeration representing the different roles in the system.
 * The roles include:
 * <ul>
 *   <li>{@link #STUDENT} - Represents a student role.</li>
 *   <li>{@link #TEACHER} - Represents a teacher role.</li>
 *   <li>{@link #ADMIN} - Represents an admin role.</li>
 * </ul>
 */
public enum Role {
    /**
     * Represents a student role.
     */
    STUDENT,

    /**
     * Represents a teacher role.
     */
    TEACHER,

    /**
     * Represents an admin role.
     */
    ADMIN
}
