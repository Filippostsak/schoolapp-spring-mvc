package gr.aueb.cf.schoolappspringbootmvc.enums;

/**
 * Enumeration representing the different statuses in the system.
 * The statuses include:
 * <ul>
 *   <li>{@link #PENDING} - Represents a pending status.</li>
 *   <li>{@link #APPROVED} - Represents an approved status.</li>
 *   <li>{@link #REJECTED} - Represents a rejected status.</li>
 * </ul>
 */
public enum Status {
    /**
     * Represents a pending status.
     */
    PENDING,

    /**
     * Represents an approved status.
     */
    APPROVED,

    /**
     * Represents a rejected status.
     */
    REJECTED
}
